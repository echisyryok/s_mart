package dao;

public class Department {
	private int dno;
	private String dname;
	public int getDno() {
		return dno;
	}
	public void setDno(int dno) {
		this.dno = dno;
	}
	public String getDname() {
		return dname;
	}
	public void setDname(String dname) {
		this.dname = dname;
	}
	public int getDbon() {
		return dbon;
	}
	public void setDbon(int dbon) {
		this.dbon = dbon;
	}
	private int dbon;

}
