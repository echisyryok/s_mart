package dao;

public class Product {

	private int pno;
	private String pname;
	private int price;
	private int psto;
	private int prop;
	private int pre;
	private int pco;
	private String pic;
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getPsto() {
		return psto;
	}
	public void setPsto(int psto) {
		this.psto = psto;
	}
	public int getProp() {
		return prop;
	}
	public void setProp(int prop) {
		this.prop = prop;
	}
	public int getPre() {
		return pre;
	}
	public void setPre(int pre) {
		this.pre = pre;
	}
	public int getPco() {
		return pco;
	}
	public void setPco(int pco) {
		this.pco = pco;
	}
	public String getPic() {
		return pic;
	}
	public void setPic(String pic) {
		this.pic = pic;
	}

}
