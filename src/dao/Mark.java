package dao;

public class Mark {
	private int hpno;
	private String pname;
	private int pno;
	private int price;
	private int hpin;
	public int getHpno() {
		return hpno;
	}
	public void setHpno(int hpno) {
		this.hpno = hpno;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getHpin() {
		return hpin;
	}
	public void setHpin(int hpin) {
		this.hpin = hpin;
	}

}
